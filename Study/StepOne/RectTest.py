from turtle import *

#设置画笔宽度
width(4)

#前进：
forward(100)
#右转90
right(90)

#笔刷颜色
pencolor('red')
forward(100)
right(90)

pencolor('green')
forward(100)
right(90)

pencolor('blue')
forward(100)
right(90)
write("Cool Python Codes",move=True,align="center",font=("Freestyle Script",50,"normal"))

#等待关闭
done()